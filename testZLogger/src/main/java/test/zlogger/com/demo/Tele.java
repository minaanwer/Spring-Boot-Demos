package test.zlogger.com.demo;

public class Tele {

      String num ;
      String numMain;

    public Tele() {

    }

    public Tele(String num, String numMain) {
        this.num = num;
        this.numMain = numMain;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getNumMain() {
        return numMain;
    }

    public void setNumMain(String numMain) {
        this.numMain = numMain;
    }


}
